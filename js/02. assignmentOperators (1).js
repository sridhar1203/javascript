// Assignment Operator	Example	Equivalent To
// = (Assignment)	a = b	a = b
// += (Addition Assignment)	a += b	a = a + b
// -= (Subtraction Assignment)	a -= b	a = a - b
// *= (Multiplication Assignment)	a *= b	a = a * b
// /= (Division Assignment)	a /= b	a = a / b
// %= (Remainder Assignment)	a %= b	a = a % b
// **= (Exponentiation Assignment)	a **= b	a = a ** b

// =  Simple assignment
let b = 10;
let c = b;        // c becomes 10
console.log(c);   // 10

// += Addition assignment
a = 10;
a += 5;           // a = 10 + 5
console.log(a);   // 15

// -= Subtraction assignment
a = 10;
a -= 3;           // a = 10 - 3
console.log(a);   // 7

// *= Multiplication assignment
a = 10;
a *= 2;           // a = 10 * 2
console.log(a);   // 20

// /= Division assignment
a = 10;
a /= 5;           // a = 10 / 5
console.log(a);   // 2

// %= Modulus assignment
a = 10;
a %= 6;           // a = 10 % 6
console.log(a);   // 4

// **= Exponentiation assignment
a = 10;
a **= 2;          // a = 10 ** 2
console.log(a);   // 100
